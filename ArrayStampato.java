public class ArrayStampato {
    public static void main (String[] args) {
        // Creazione di un array di numeri
        int[] numeri = {23, 44, 1, 64, 97};

        // Ciclo for per stampare ogni elemento
        for (int i = 0; i < numeri.length; i++) {
            System.out.println("Elemento in posizione " + i + ": " + numeri[i]);
        }
    }
}

public class Operazioni {
    public static void main(String[] args) {
        int a = 5;
        int b = 3;
        System.out.println("La somma di " + a + " + " + b + " è uguale a " + (a + b));
        System.out.println("La differenza di " + a + " - " + b + " è uguale a " + (a - b));
        System.out.println("Il prodotto di " + a + " * " + b + " è uguale a " + (a * b));
    }
}
